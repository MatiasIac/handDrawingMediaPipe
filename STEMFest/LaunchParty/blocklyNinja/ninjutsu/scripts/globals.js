function hsbToRgb(h, s, b) {
    const c = b * s;
    const x = c * (1 - Math.abs((h / 60) % 2 - 1));
    const m = b - c;

    let r = 0, g = 0, b_ = 0;

    if (h >= 0 && h < 60)      [r, g, b_] = [c, x, 0];
    else if (h < 120)          [r, g, b_] = [x, c, 0];
    else if (h < 180)          [r, g, b_] = [0, c, x];
    else if (h < 240)          [r, g, b_] = [0, x, c];
    else if (h < 300)          [r, g, b_] = [x, 0, c];
    else                       [r, g, b_] = [c, 0, x];

    return {
        r: Math.floor((r + m) * 255),
        g: Math.floor((g + m) * 255),
        b: Math.floor((b_ + m) * 255)
    };
}

function getJollyColor() {
    const hue = Math.floor(Math.random() * 360);      // Full rainbow
    const saturation = 1.0;                           // Max saturation
    const brightness = 1.0;                           // Max brightness
    return hsbToRgb(hue, saturation, brightness);
}